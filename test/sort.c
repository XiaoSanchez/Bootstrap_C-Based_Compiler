#include <stdio.h>
#include <stdlib.h>
int a[] = {1,9,0,7,8,2,6,3,5,3,4,1,1};
const int array_size = 13;
void BubbleSort(int size)
{
    int i, j, temp;
    for (i = 0; i < (size - 1); ++i)
    {
        for (j = 0; j < size - 1 - i; ++j )
        {
            if (a[j] > a[j+1])
            {
                temp = a[j+1];
                a[j+1] = a[j];
                a[j] = temp;
            }
        }
    }
}
int main() {
    BubbleSort(array_size);
    for (int i = 0 ; i < array_size; i++) {
        printf ("%d ", a[i]);
    }
    return 0;
}
